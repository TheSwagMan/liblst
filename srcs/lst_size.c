#include "liblst.h"

int	lst_size(t_lst *lst)
{
	int	n;

	n = 0;
	if (!lst)
		return (-1);
	lst_goto_n(&lst, 0);
	while (lst)
	{
		n++;
		lst = lst->next;
	}
	return (n);
}
